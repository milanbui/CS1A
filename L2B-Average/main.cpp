#include <iostream>
using namespace std;

int main ()
{
	double num1, num2, num3, average;

	num1 = 5.0;
	num2 = 10.0;
	num3 = -12.6;

	average = (num1+num2+num3)/3;

	cout << "The first number is " << num1 << endl;
	cout << "The second number is " << num2 << endl;
	cout << "The third number is " << num3 << endl;
	cout << "The average is " << average << endl;

	return 0;
}



